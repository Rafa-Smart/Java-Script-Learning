
// jadi di dalam js itu kita bisa mengatur dan mengecek apakah sebuah data itu null atau undefined,
// caranya adalah menggunakan ?? operator

// nah jadi jika datanya itu bernilai null atau undefined, maka kita akan isi nilai defaultnya menggunakan ?? caranya adlah gini


let data1 = null;
console.log(data1 ?? "ini data nilai default"); // Output: ini data nilai default
